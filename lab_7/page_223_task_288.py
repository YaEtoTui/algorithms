import numpy as np
from scipy.optimize import linprog

# Целевая функция: -5x1 - x2 + 8x3 + 10x4 - 5x5 + x6
c = [-5, -1, 8, 10, -5, 1]

# Коэффициенты ограничений
A = [
    [2, -1, 0, 0, 0, 0],  # 2x1 - x2 = 40
    [-1, 2, -1, 0, 0, 0],  # -x1 + 2x2 - x3 = 20
    [3, 0, 0, 1, 0, 0],  # 3x1 + x4 = 30
    [0, 0, 2, 0, 1, 0],  # 2x3 + x5 = 20
    [0, 0, 3, 0, 0, 1]   # 3x3 + x6 = 40
]

# Правые части ограничений
b = [40, 20, 30, 20, 40]

# Типы ограничений: равенство
bounds = [(0, None) for _ in range(6)]  # x1, x2, x3, x4, x5, x6 >= 0

# Решение задачи линейного программирования
res = linprog(c, A_eq=A, b_eq=b, bounds=bounds, method='highs')

# Вывод результатов
if res.success:
    print("Оптимальное значение целевой функции:", -res.fun)
    print("Значения переменных:", res.x)
else:
    print("Решение не найдено")
