import numpy as np
from scipy.optimize import linprog

# Коэффициенты целевой функции (для максимизации берем с противоположным знаком)
c = [-10, -14, -12]

# Матрица коэффициентов ограничений
A = [
    [4, 2, 1],
    [3, 1, 3],
    [1, 2, 5]
]

# Вектор правых частей ограничений
b = [180, 210, 244]

# Границы переменных (все переменные неотрицательные)
x_bounds = (0, None)

# Решение задачи линейного программирования
res = linprog(c, A_ub=A, b_ub=b, bounds=[x_bounds, x_bounds, x_bounds], method='simplex')

# Вывод результатов
if res.success:
    print("Оптимальные значения переменных:")
    print(f"x1 = {res.x[0]:.2f}")
    print(f"x2 = {res.x[1]:.2f}")
    print(f"x3 = {res.x[2]:.2f}")
    print(f"Максимальное значение целевой функции: {-res.fun:.2f}")
else:
    print("Решение не найдено")
