import numpy as np
from scipy.optimize import linprog

# Коэффициенты целевой функции (максимизация, поэтому берем с противоположным знаком)
c = [-2, -3, 1, 2]

# Коэффициенты левых частей неравенств
A = [
    [1, 1, -2, 1],   # x1 + x2 - 2x3 + x4 <= 17
    [-3, 1, 0, 0],   # -3x1 + x2 <= 9
    [4, -1, 0, 0],   # 4x1 - x2 <= 8
    [0, 0, 1, 1],    # x3 + x4 <= 22
    [0, 0, -2, 1]    # -2x3 + x4 <= 13
]

# Правые части неравенств
b = [17, 9, 8, 22, 13]

# Границы переменных (x1, x2, x3, x4 >= 0)
x_bounds = (0, None)
bounds = [x_bounds, x_bounds, x_bounds, x_bounds]

# Решение задачи линейного программирования
result = linprog(c, A_ub=A, b_ub=b, bounds=bounds, method='highs')

# Вывод результатов
if result.success:
    print("Оптимальное значение функции:", -result.fun)  # берем с противоположным знаком, т.к. ищем максимум
    print("Оптимальные значения переменных:", result.x)
else:
    print("Решение не найдено")
